package src;

/**
 * Clase que proporciona operaciones aritméticas básicas.
 */
public class Calculadora {

    /**
     * Realiza la suma de dos números enteros.
     * @param a El primer número entero.
     * @param b El segundo número entero.
     * @return La suma de a y b.
     */
    public int suma(int a, int b) {
        return a + b;
    }

    /**
     * Realiza la resta de dos números enteros.
     * @param a El minuendo (primer número entero).
     * @param b El sustraendo (segundo número entero).
     * @return La diferencia entre a y b (a - b).
     */
    public int resta(int a, int b) {
        return a - b;
    }
}
